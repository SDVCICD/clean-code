public class Calcul {

    /**
     * fonction retournant l'addition de a + b
     * @param a
     * @param b
     * @return
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * fonction retournant la soustraction de a - b
     * @param a
     * @param b
     * @return
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
